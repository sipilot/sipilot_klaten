  # POST /resource
  def create
    user = User.new(username: user_params['username'], password: user_params['password'], email: user_params['email'])
    user.build_user_detail(fullname: user_params['fullname'], id_number: user_params['id_number'])
    if user.save
      UserRole.create(user_id: user.id, role_id: Role.member_sipilot_id) # set default user register has member role
      sign_in(user)
      redirect_to dashboards_path
      return
    end

    redirect_to new_user_registration_path(flash: user.errors.full_messages)
  end

